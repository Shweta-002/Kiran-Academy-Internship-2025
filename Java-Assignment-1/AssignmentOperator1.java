
public class AssignmentOperator1 {
	public static void main(String arg[]) {
		int a = 25;
		System.out.println("By addition: " + (a+=2));
		System.out.println("By subtraction: " + (a-=3));
		System.out.println("By multiply: " + (a*=4));
		System.out.println("By division: "+ (a/=5));
		System.out.println("By modulus: " + (a%=6));
	}

}
